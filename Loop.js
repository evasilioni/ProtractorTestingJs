// For Loop with increment value

// Print a table of any number 
function anyNumberTbl(){
    var number = 8;

    for(i=1; i<=10;i++){
        console.log(number*i);
    };
};

// Print a table of any number but in reverse ordrer

function anyReverseNumberTbl(){
    var number = 8;

    for(i=10; i>=1;i--){
        console.log(number*i);
    };
};


anyReverseNumberTbl();

 


